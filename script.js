const productContainer = document.getElementById("products");

// RANDOM SHUFFLE FUNCTION
function shuffleProducts(array) {
  let shuffled = [...array];

  for (let i = shuffled.length - 1; i > 0; i--) {
    let j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }

  return shuffled;
}

function openProduct(id) {
  window.location.href = `Detail.html?id=${id}`;
}

// UNIVERSAL DISPLAY FUNCTION
function displayProducts(category = "all", limit = 9) {
  let filteredProducts;

  // HOMEPAGE (random products)
  if (category === "all") {
    filteredProducts = shuffleProducts(products).slice(0, limit);
  }

  // COLLECTION PAGES (category products)
  else {
    filteredProducts = products.filter(
      (product) => product.category === category,
    );
  }

  let html = "";

  filteredProducts.forEach((product) => {
    html += `
      <div class="product-item"  onclick="openProduct(${product.id})">
      <img src="${product.images ? product.images[0] : product.image}" alt="${product.name}">
        <div class="product-content">
          <p>${product.name} ₹${product.price}</p>
        </div>
      </div>
    `;
  });

  productContainer.innerHTML = html;
}
// GET PRODUCT ID FROM URL
const params = new URLSearchParams(window.location.search);
const productId = parseInt(params.get("id"));

// FIND PRODUCT FROM DATABASE
const product = products.find((p) => p.id === productId);

if (product) {
  // TEXT DATA
  document.getElementById("product-title").innerText = product.name;
  document.getElementById("product-price").innerText =
    "₹" + product.price.toLocaleString("en-IN");

  document.getElementById("product-desc").innerText =
    "Premium boutique designer outfit from Shakshi's Boutique collection.";

  /* --------------------------------------------------
     SUPPORT BOTH image AND images ARRAY
  -------------------------------------------------- */

  let productImages = [];

  if (product.images && product.images.length > 0) {
    productImages = product.images; // multiple images
  } else {
    productImages = [product.image]; // single image fallback
  }

  // MAIN IMAGE
  const mainImage = document.getElementById("mainImage");
  mainImage.src = productImages[0];

  // THUMBNAILS
  const thumbsContainer = document.getElementById("thumbs");
  thumbsContainer.innerHTML = "";

  productImages.forEach((imgSrc, index) => {
    const img = document.createElement("img");
    img.src = imgSrc;
    img.classList.add("thumb");

    if (index === 0) img.classList.add("active");

    img.addEventListener("click", () => {
      mainImage.src = imgSrc;

      document
        .querySelectorAll(".thumb")
        .forEach((t) => t.classList.remove("active"));

      img.classList.add("active");
    });

    thumbsContainer.appendChild(img);
  });
}

